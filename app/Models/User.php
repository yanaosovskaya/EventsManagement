{
    use Notifiable, SoftDeletes, AccessTrait;

    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'first_name', 'last_name', 'email', 'password', 'active'

    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
