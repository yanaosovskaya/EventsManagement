        'password', 'remember_token',
    ];

    /**
     * The attributes that should be mutated to dates.
     *
     * @var array
     */
    protected $dates = ['deleted_at'];

    /**
     * @return int
     */
    public function isActive()
    {
        return $this->active;
    }

    public function profile()
    {
        return $this->hasOne(Profile::class);
